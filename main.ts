function spawnsomthing (roll: number) {
    if (roll <= 2) {
        createSaucer
    } else if (roll <= 6) {
        createTree()
    } else if (roll <= 24) {
        createCloud()
    } else if (roll <= 40) {
        createBird()
    }
}
function placeMontain (leftPosition: number) {
    lastCreatedMonutain = sprites.create(mountains[randint(0, 1)], SpriteKind.Mountain)
    lastCreatedMonutain.left = leftPosition
    lastCreatedMonutain.bottom = scene.screenHeight()
    lastCreatedMonutain.z = -15
    lastCreatedMonutain.setFlag(SpriteFlag.Ghost, true)
    lastCreatedMonutain.setFlag(SpriteFlag.AutoDestroy, true)
}
